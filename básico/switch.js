const cor = 'verde';

switch (cor) {
    case "vermelho":
        console.log('Sinal fechado');
        break;

    case "amaralho":
        console.log('Atenção')
        break

    case "verde":
        console.log('Avançar')
        break
        
    default: 
        console.log('Cor inválida')
}